package switchexample;

public class SwitchExample3 {
	
public static void main(String[] args) {
		
		String Continent="Africa";
		
		switch(Continent) {
		case "Africa":
			System.out.println("you are in Africa");
			break;
		case "Asia":
			System.out.println("you are in Asia");
			break;
		case "Europe":
			System.out.println("you are in Europe");
			break;
		case "North America":
			System.out.println("you are in North America");
			break;
		case "South America":
			System.out.println("you are in South America");
			break;
		case "Australia":
			System.out.println("you are in Australia");
			break;
			
		default:
			System.out.println("not a valid continent");
			
			System.out.println("End of program");
		
	}
}}



